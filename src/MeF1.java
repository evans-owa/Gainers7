public class MeF1 {
    public static void main(String[] args) {
//        the parenthesis  is not a must is used to for showing clearance
         boolean butT = 2==3; // false
         boolean manT = (4*7) != (4*7); //false
        System.out.println(butT);
        System.out.println(manT);

        boolean q2 = 2>=5; // false
        boolean t2 = 2>5; // false
        System.out.println(q2);
        System.out.println(t2);

        boolean l1 = 1<1; // false
        boolean l2 = 2<=5; //true
        System.out.println(l1);
        System.out.println(l2);

    }
}
